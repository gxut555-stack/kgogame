//加密
func Encrypt(data []byte, iv int) []byte {
	ret := make([]byte, base64.StdEncoding.EncodedLen(len(data)))
	base64.StdEncoding.Encode(ret, data)
	if iv < 0 {
		iv = -iv
	}
	iv %= 256
	for k, v := range ret {
		ret[k] = encMap[(int(v)+iv)%256]
	}
	return ret
}

//解密
func Decrypt(data []byte, iv int) ([]byte, error) {
	if iv < 0 {
		iv = -iv
	}
	iv %= 256
	for k, v := range data {
		data[k] = byte((int(decMap[v]) + 256 - iv) % 256)
	}
	return base64.StdEncoding.DecodeString(string(data))
}
